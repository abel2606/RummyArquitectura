    private void botonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConfirmarActionPerformed
        String nombre = textFieldNombre.getText();
        String avatar = "";
        if (!nombre.isBlank()) {
            if (grupoImagenes.getSelection() != null) {
                if (avatar1.isSelected()) {
                    avatar = "awa";
                } else if (avatar2.isSelected()) {
                    avatar = "ewe";
                } else if (avatar3.isSelected()) {
                    avatar = "iwi";
                } else if (avatar4.isSelected()) {
                    avatar = "owo";
                } else if (avatar5.isSelected()) {
                    avatar = "uwu";
                } else if (avatar6.isSelected()) {
                    avatar = "ywy";
                }
                
                if (host) {
                    control.iniciarSeleccionarColores(nombre, avatar);
                } else {
                    control.enviarInformacionAnfitrion(nombre, avatar);
                }
                setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un avatar.",
                        "Alerta", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre de usuario.",
                    "Alerta", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botonConfirmarActionPerformed

